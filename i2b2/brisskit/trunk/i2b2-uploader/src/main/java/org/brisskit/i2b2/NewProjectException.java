package org.brisskit.i2b2;

public class NewProjectException extends Exception {

	public NewProjectException() {
		super();
	}

	public NewProjectException(String message, Throwable cause) {
		super(message, cause);
	}

	public NewProjectException(String message) {
		super(message);
	}

	public NewProjectException(Throwable cause) {
		super(cause);
	}
	
}